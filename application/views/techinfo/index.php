            Прежде всего нужно пояснить, что наибольший интерес представляют не фотографии разъемов и их номера(они чисто условные), а соответствия между моделями телефонов и разъемами. Один и тот же разъем может быть в нескольких исполнениях. В моей классификации это, например, номер 5 (знаменитый разъем типа Lenovo P780), который используется в огромном колличестве различных девайсов. Таким исполнениям одного и того же разъема я стараюсь присваивать один номер, чтобы не умножать сущности сверх надобности. Иногда разъемы отличаются друг от друга не очень сильно, и их в крайнем случае можно ставить друг вместо друга (возможно, придется подпилить разъем или корпус телефона). Такие пары разъемов я определяю как аналоги. Например, для разъема номер 5 это разъем номер 73 (xiaomi redmi note 4). Они немного отличаются по высоте. Прошу сильно не пинать, это только попытка и работа продолжается, но катастрофически не хватает времени и, что самое главное, нет такого потока ремонтов, чтобы быстро собрать нужную инфу. Я не так давно этим занялся системно, где-то с начала октября 2018г. Что-то знал и помнил сам, что-то нарыл в интернете, что-то принесли в ремонт за это время. Хочется сказать, что в интернет-магазинах, продающих разъемы невероятное количество ошибок! Наверняка и у меня их много, но работа ведется. Очень надеюсь на помощь сообщесва. Информацию удобно посылать через форму обратной связи на сайте или на mikolahh@gmail.com
