          <p>Ни для кого не секрет, что существует огромное количество моделей мобильных телефонов и планшетов, которое постоянно увеличивается. Постоянно возникают ситуации, когда сталкиваешься с каким-нибудь девайсом в первый раз, и, очень вероятно, что следующий раз произойдет не скоро, а вполне может быть, что никогда. В таких условиях невозможно накопление опыта. Когда-то можно было совершить ошибку при ремонте конкретного устройства, но сделать выводы, чтобы в последующих случаях ее не повторять, и отыграться на последующих ремонтах. Сейчас таковых может и не быть, или быть очень мало. Более того, все чаще возникают ситуации, когда решение о ремонте или об отказе от него нужно принимать буквально в течении нескольких десятков секунд, и совершенно нет времени смотреть ролик в интернете, чтобы, к примеру, определить время, необходимое для ремонта (успеешшь за 20 минут или нет?!). Возникает необходимость в каком-то системном накоплении информации, крайне желательно в совместном. Мною предлагается попытка такой системы, сам ей пользуюсь, и уже было много случаев, когда она помогала. В первую очередь это информация о разъемах зарядки. Фотографии платы помогают быстро определить, к примеру, какой разъем гарнитуры, сим-коннектор или микрофон используется в устройстве, очень помогают комментарии типа "для разборки аппарата необходимо отклеивать дисплейный модуль" или "разъем зарядки может быть залит компаундом". Буду рад любой информации подобного рода, которая обязательно будет внесена в базу максимум в течении недели. Для этой цели удобно использовать форму обратной связи на сайте, если передается только текст. Фотографии плат (я их делаю обычным сотиком в формате 4x3) можно отправлять на mikolahh@gmail.com</p>  

            
