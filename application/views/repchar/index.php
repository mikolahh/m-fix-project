    <div class="row">
          <p>
            1. Если аппарат долгое время не включался, то вполне вероятно, что аккумулятор очень сильно разрядился и устройство его 'не видит'. В этом случае необходима  принудительная зарядка аккумуляторной батареи. Такая процедура производится бесплатно (если не требуется разборка) или за небольшую вменяемую сумму (в зависимости от сложности разборки).
            <br>
            2. В некоторых случаях возможен  сбой программного обеспечения. В этом случае необходимо пробовать долго (до 20-30 секунд) удерживать  кнопку включения как отдельно, так и в сочетании с другими кнопками. Можно пробовать подключать в это время зарядное устройство. Иногда помогает только переподключение аккумуляторной батареи.
            <br>            
            3. Несправность в цепи зарядки: зарядное устройство, кабель, разъем зарядки. Последнее происходит наиболее часто. В этом случае необходима замена системного разъема.
            <br>
            4. Вышла из строя основная плата. Это самый тяжелый случай. Ремонт таких устройств чаще всего бывает экономически нецелесообразен.  
          </p>            
