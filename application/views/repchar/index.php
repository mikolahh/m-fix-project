            <br>
            2. Неисправность аккумуляторной батареи. В этом случае происходит её быстрый заряд и такой же быстрый разряд. Характерным внешним признаком неисправной батареи является её 'вздутие'.
            Следует знать, что купить оригинальную акб практически невозможно. Те 'оригинальные', которые продаются, пусть в разной степени, но хуже качеством. Можно посоветовать следующее правило: если зарядки стабильно хватает на день, то не стоит менять частично выработавшую свой ресурс оригинальную батарею.
