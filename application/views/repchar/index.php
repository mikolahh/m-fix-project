          <p>
            1. Прежде всего следует обратить внимание на кабель. В идеале, конечно же, использование оригинального из комплекта. Если таковой отсутствует, то желательно найти аналогичный оригинальный кабель из комплекта другого устройства независимо от модели и производителя. Если такой возможности нет, то лучше всего не тратить время зря, а сразу же посетить мастерскую M-FIX, чтобы решить все вопросы. Не стоит пытаться покупать где-то 'китайский' кабель, так как в 95-99% случаев везде продается такой откровенный мусор, что просто нет слов.
            <br>

            2. Гораздо реже проблемы возникают с самим зарядным устройством, особенно если оно оригинальное из комплекта. Ни в коем случае не стоит его выбрасывать, если сам аппарат вышел из строя. Оно почти всегда подойдет к другому устройству, а купить аналогичное по качеству практически невозможно, только б.у. из комплекта другого аппарата. Не стоит бояться таких зарядок, они не изнашиваются, как кабели, и могут проработать лет сто, наверное.
            <br>
            3. Возможно также, что причина в сильно изношенном разъеме зарядки. В этом случае необходима его замена.
            <br>
            4. И,  наконец, возможна неисправность основной платы устройства, но это можно выяснить только в услоиях мастерской. 
