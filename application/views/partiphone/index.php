      <h1>Особенности ремонта Iphone</h1>
      <br>
      <p class="lead" style="">
      	Мобильные телефоны фирмы Apple пользуются огромной популярностью во всем мире и занимают особое положение среди прочих брендов. Они отличаются хорошей ремонтопригодностью, в свободном  доступе имеются почти все комплектующие, возможно, не оригинальные, но в том числе и вполне приемлимого качества. Однако, как это обычно случается, у такой популярности имеется и оборотная сторона. Широкое распространение получили восстановленные аппараты, так называемые rf-ы (refurbished), к которым производитель не имеет никакого отношения. Как правило, оригинальная у них только материнская плата, которая скорее всего после ремонта. Такие изделия приобретаются через интернет, а часто и во вполне приличных магазинах, из-за чего их удельный вес в общем объеме весьма значителен. Зачастую сами хозяева не знают, что они приобрели, и, когда такие телефоны попадают в ремонт или для замены батареи, возникают различные нежелательные ситуации. Существуют и другие сложности нетехнического характкра, что приводит в конечном итоге к увеличению стоимости ремонта Iphone, так как в нее приходится закладывать дополнительную плату за риск. Таким образом, один из основных принципов обслуживания M-FIX (<strong> "Заказчик платит только за ремонт своего изделия без включения в стоимость ремонта скрытых дополнительных издержек"</strong>) при ремонте Iphone, к сожалению, не действует.
      </p>

